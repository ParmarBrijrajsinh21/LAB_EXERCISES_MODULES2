//3. Variables constants , Datas Tpyes, And Comments .
#include <stdio.h>
int main() 
{
    // This is a single-line comment
    /* This is a
       multi-line comment */

    // Declaring variables
    int age = 18;          
    float height = 5.7;    
    char grade = 'A';      

    // Declaring constant
    const float PI = 3.14;
    // Displaying values
    printf("Age (int) = %d\n", age);
    printf("Height (float) = %.2f\n", height);
    printf("Grade (char) = %c\n", grade);
    printf("Constant PI = %.2f\n", PI);
}
